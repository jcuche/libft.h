#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*oui;

	oui = (char *)s;
	i = ft_strlen(oui);
	while (i > 0 && s[i] != (char)c)
		i--;
	if (s[i] == (char)c)
		return (&((char *)s)[i]);
	return (NULL);
}
