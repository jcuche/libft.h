#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	a;

	a = 0;
	while (s1[a] && s2[a] && s1[a] == s2[a] && n > 0)
	{
		if (s1[a] != s2[a])
		{
			break ;
		}
		a++;
		n--;
	}
	if (n == 0)
	{
		return (0);
	}
	return (s1[a] - s2[a]);
}
