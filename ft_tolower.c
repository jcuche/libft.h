#include "libft.h"

int	ft_tolower(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if (c >= 65 && c <= 90)
	{
		ch = ch + 32;
		c = (int)ch;
	}
	return (c);
}
